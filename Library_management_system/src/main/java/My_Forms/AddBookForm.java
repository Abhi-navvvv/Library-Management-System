    public boolean verif()
    {
        if(jTextField_ISBN.getText().equals("") || jTextField_Author.getText().equals("") ||
                jTextField_Price.getText().equals("") || jTextField_Name.getText().equals("") || 
                jLabel_Genre_ID.getText().equals("ID")){
                return false;
        }
        else{
                return true;
            }
    }
    
